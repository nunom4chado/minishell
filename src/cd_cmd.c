#include "minishell.h"

void	cd_cmd(char *cmd)
{
	if (chdir(cmd + 3) < 0)
	{
		if (cmd[0] == 'c' && cmd[1] == 'd' && cmd[2] == ' ' && (cmd[3] == '\0' || cmd[3] == '~'))
			chdir(getenv("HOME"));
		else
			printf("cannot cd %s\n", cmd + 3);
	}
}
